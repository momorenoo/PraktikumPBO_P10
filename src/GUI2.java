    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        // TODO add your handling code here:
        String nama, nim, prodi, notelp;
        nama    = TFnama.getText();
        nim     = TFnim.getText();
        prodi   = TFprodi.getText();
        notelp  = TFnotelp.getText();
        Object[] data = {nama,nim,prodi,notelp};
        model.addRow(data);
        try {
            Connection con = Koneksi.GetConnection();
            String query = "INSERT INTO mahasiswa(nama,nim,prodi,notelp) VALUES(?,?,?,?)";
            PreparedStatement p = con.prepareStatement(query);
            
            p.setString(1,nama);
            p.setString(2,nim);
            p.setString(3,prodi);
            p.setString(4,notelp);
            
            p.executeUpdate();
            p.close();
            System.out.println("Executed");
            TFnama.setText("");
            TFnim.setText("");
            TFprodi.setText("");
            TFnotelp.setText("");
        }
        catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btntambahActionPerformed

