    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblmhs.getSelectedRow();
        if(selectedRowIndex == -1){
            JOptionPane.showMessageDialog(this, "Tidak ada tabel yang dipilih.");
        }
        else {
            String nim = tblmhs.getValueAt(selectedRowIndex, 1).toString();
            
            try {
                Connection con = Koneksi.GetConnection();
                String query = "DELETE FROM mahasiswa where nim = ?";
                PreparedStatement p = con.prepareStatement(query);
                p.setString(1,nim);
            
                p.executeUpdate();
                p.close();
                
                DefaultTableModel model = (DefaultTableModel) tblmhs.getModel();
                model.removeRow(selectedRowIndex);
                
                JOptionPane.showMessageDialog(this, "Tabel berhasil dihapus.");
            }
            catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal Menghapus data");
            }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

