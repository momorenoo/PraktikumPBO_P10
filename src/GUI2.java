    private void tampilData() {
        String nama,nim,prodi,notelp;
        try {
            Connection con = Koneksi.GetConnection();
            Statement st = con.createStatement();
            
            String query = "SELECT nama,nim,prodi,notelp FROM mahasiswa";
            ResultSet rs = st.executeQuery(query);
            
            DefaultTableModel model = (DefaultTableModel) tblmhs.getModel();
            model.setRowCount(0);
            
            while(rs.next()) {
                nama = rs.getString("nama");
                nim = rs.getString("nim");
                prodi = rs.getString("prodi");
                notelp = rs.getString("notelp");
                Object[] data = {nama,nim,prodi,notelp};
                model.addRow(data);
            }
        }
        catch (Exception e) {
            e.printStackTrace();
        }
    }
